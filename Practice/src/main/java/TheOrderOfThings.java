public class TheOrderOfThings {

    public static void main(String[] args) {

        double number;
        String opinion, size, age, shape, color, origin, material, purpose;
        String noun;

        number = 5.0;
        opinion = "AWESOME";
        size = "teensy-weensy";
        age = "new";
        shape = "oblong";
        color = "BRIGHT yellow";
        origin = "AlphaCentaurian";
        material = "platinum";
        purpose = "good";

        noun = "dragons";

        System.out.println(number + " " + opinion  + " " + size  + " " + age  + " " +  shape  + " " +  color
                + " " +  origin  + " " +  material  + " " +  purpose  + " " +  noun);

        System.out.println(number + " " + material  + " " + size  + " " + color  + " " +  shape  + " " +  age
                + " " +  origin  + " " +  opinion  + " " +  purpose  + " " +  noun);
    }
}
